// Function to get SubBytes
unsigned char sub_bytes()
{
    int i, j;
    for(i = 0; i < 4; ++i)
    {
        for(j = 0; j < 4; ++j)
        {
            tt[j][i] = s[tt[j][i]];
        }
    }
}

// Function to get inverse of SubBytes
unsigned char inv_sub_bytes()
{
    int i, j;
    for(i = 0; i < 4; ++i)
    {
        for(j = 0; j < 4; ++j)
        {
            tt[j][i] = inv_s[tt[j][i]];
        }
    }
}


// Mix Columns implementation
// Number of times to be multiplied
static unsigned char xtime(unsigned char x)
{
	return ((x<<1) ^ (((x>>7) & 1) * 0x1b));
}

// Multiply is used to multiply numbers in the field GF(2^8)
#if MULTIPLY_AS_A_FUNCTION
static unsigned char Multiply(unsigned char x, unsigned char y)
{
  return (((y & 1) * x) ^
       ((y>>1 & 1) * xtime(x)) ^
       ((y>>2 & 1) * xtime(xtime(x))) ^
       ((y>>3 & 1) * xtime(xtime(xtime(x)))) ^
       ((y>>4 & 1) * xtime(xtime(xtime(xtime(x))))));
  }
#else
#define Multiply(x, y)                                \
      (  ((y & 1) * x) ^                              \
      ((y>>1 & 1) * xtime(x)) ^                       \
      ((y>>2 & 1) * xtime(xtime(x))) ^                \
      ((y>>3 & 1) * xtime(xtime(xtime(x)))) ^         \
      ((y>>4 & 1) * xtime(xtime(xtime(xtime(x))))))   \

#endif

// Function to get MixColumns (For encryption)
static void mix_columns()
{
  unsigned char i;
  unsigned char Tmp,Tm,t;
  for(i = 0; i < 4; ++i)
  {  
    t   = tt[i][0];
    Tmp = tt[i][0] ^ tt[i][1] ^ tt[i][2] ^ tt[i][3] ;
    Tm  = tt[i][0] ^ tt[i][1] ; Tm = xtime(Tm);  tt[i][0] ^= Tm ^ Tmp ;
    Tm  = tt[i][1] ^ tt[i][2] ; Tm = xtime(Tm);  tt[i][1] ^= Tm ^ Tmp ;
    Tm  = tt[i][2] ^ tt[i][3] ; Tm = xtime(Tm);  tt[i][2] ^= Tm ^ Tmp ;
    Tm  = tt[i][3] ^ t ;        Tm = xtime(Tm);  tt[i][3] ^= Tm ^ Tmp ;
  }
}

// Function to get MixColumns (For decryption)
static void inv_mix_columns()
{
  int i;
  unsigned char a,b,c,d;
  for(i=0;i<4;++i)
  { 
    a = tt[i][0];
    b = tt[i][1];
    c = tt[i][2];
    d = tt[i][3];

    tt[i][0] = Multiply(a, 0x0e) ^ Multiply(b, 0x0b) ^ Multiply(c, 0x0d) ^ Multiply(d, 0x09);
    tt[i][1] = Multiply(a, 0x09) ^ Multiply(b, 0x0e) ^ Multiply(c, 0x0b) ^ Multiply(d, 0x0d);
    tt[i][2] = Multiply(a, 0x0d) ^ Multiply(b, 0x09) ^ Multiply(c, 0x0e) ^ Multiply(d, 0x0b);
    tt[i][3] = Multiply(a, 0x0b) ^ Multiply(b, 0x0d) ^ Multiply(c, 0x09) ^ Multiply(d, 0x0e);
  }
}


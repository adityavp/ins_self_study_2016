// This function produces Nb(Nr+1) round keys. The round keys are used in each round to decrypt the states. 
// github.com/kokke/tiny-AES128-C
static void KeyExpansion(void)
{
  unsigned int i, j, k;
  unsigned char tempa[4]; // Used for the column/row operations
  
  // The first round key is the key itself.
  for(i = 0; i < Nk; ++i)
  {
    RoundKey[(i * 4) + 0] = Key[(i * 4) + 0];
    RoundKey[(i * 4) + 1] = Key[(i * 4) + 1];
    RoundKey[(i * 4) + 2] = Key[(i * 4) + 2];
    RoundKey[(i * 4) + 3] = Key[(i * 4) + 3];
  }

  // All other round keys are found from the previous round keys.
  for(; (i < (Nb * (Nr + 1))); ++i)
  {
    for(j = 0; j < 4; ++j)
    {
      tempa[j] = RoundKey[(i-1) * 4 + j];
    }
    if (i % Nk == 0)
    {
      // This function rotates the 4 bytes in a word to the left once.
      // [a0,a1,a2,a3] becomes [a1,a2,a3,a0]

      // Function RotWord()
      {
        k 		 = tempa[0];
        tempa[0] = tempa[1];
        tempa[1] = tempa[2];
        tempa[2] = tempa[3];
        tempa[3] = k;
      }

      // SubWord() is a function that takes a four-byte input word and 
      // applies the S-box to each of the four bytes to produce an output word.

      // Function Subword()
      {
        tempa[0] = s[tempa[0]];
        tempa[1] = s[tempa[1]];
        tempa[2] = s[tempa[2]];
        tempa[3] = s[tempa[3]];
      }

      tempa[0] =  tempa[0] ^ Rcon[i/Nk];
    }
    else if (Nk > 6 && i % Nk == 4)
    {
      // Function Subword()
      {
        tempa[0] = s[tempa[0]];
        tempa[1] = s[tempa[1]];
        tempa[2] = s[tempa[2]];
        tempa[3] = s[tempa[3]];
      }
    }
    RoundKey[i * 4 + 0] = RoundKey[(i - Nk) * 4 + 0] ^ tempa[0];
    RoundKey[i * 4 + 1] = RoundKey[(i - Nk) * 4 + 1] ^ tempa[1];
    RoundKey[i * 4 + 2] = RoundKey[(i - Nk) * 4 + 2] ^ tempa[2];
    RoundKey[i * 4 + 3] = RoundKey[(i - Nk) * 4 + 3] ^ tempa[3];
  }
}

// Function to add round key to state using XOR.
static void AddRoundKey(unsigned char round)
{
	unsigned char i, j;
	for(i = 0; i < 4; ++i)
	{
		for(j = 0; j < 4; ++j)
		{
			tt[i][j] ^= RoundKey[round * Nb * 4 + i * Nb + j];
		}
	}
}





void encrypt()
{
	copy_matrix(pt, tt);

	AddRoundKey(0);

	for(int i = 1; i <= Nr-1; ++i)
	{
		sub_bytes();
		shift_rows();
		mix_columns();
		AddRoundKey(i);
	}

	sub_bytes();
	shift_rows();
	AddRoundKey(Nr);

	copy_matrix(tt, ct);
}

void decrypt()
{
	AddRoundKey(Nr);

	for(int i = Nr-1; i > 0; i--)
	{
		inv_shift_rows();
		inv_sub_bytes();
		AddRoundKey(i);
		inv_mix_columns();
	}

	inv_shift_rows();
	inv_sub_bytes();
	AddRoundKey(0);
}

